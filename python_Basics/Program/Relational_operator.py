# 2. Relational Operator ===> >, <, >=, <=, != , ==
a = 4
b = 8

# greater >
print("> operator :",  a > b) # False

# less <
print("< operator :", a < b) # True

# greater than or equal to >=
print(">= operator :", a >= b) # False

# less than or equal to <=
print("<= operator :", a <= b) # True

# not equal to !=
print("!= operator :", a != b) # True

# equal equal to ==
print("== operator :", a == b) # False
