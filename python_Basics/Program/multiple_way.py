# 11. Multiple way to Define Incre / Decre
z = 2
# Method 1 Increment and Decrement
# Increment
z += 4
print("Method 1 Increment :", z)

# Decrement
z -= 3
print("Method 1 Decrement :", z)


# Method 2 Increment and Decrement
# Increment
y = 6
y = y + 1
print("Method 2 Increment :", y)

# Decrement
y = y - 2
print("Method 2 Decrement:", y)
