# Example for "while loop"
# print 1 to 20 using while loop
# print 20 to 1 using while loop

var = 1
while var <= 20:
    print(var, end = " ")
    var = var + 1 # increment
print("\n") 
var = 20
while var >= 1:
    print(var, end = " ")
    var = var - 1 # decrement