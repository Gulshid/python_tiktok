# Example for "if - elif - else statement"
num = 5
if num == 1:
    print("num is 1")
    
elif num == 2:
    print("num is 2")
    
elif num == 3:
    print("num is 3")
    
elif num == 4:
    print("num is 4")
    
elif num == 5:
    print("num is 5")
    
else:
    print("The num value is neither 1,2,3,4,5")
    
    