# 5. Arthematic and Assignement Operator ===> +=, -=, *=, /=, %=, =
# = operator
x = 3
print("=:", x)
# updated value ==> 3
# += operator 
x += 3
print("+=:", x)
# updated value ==> 6

# -= operator
x -= 2
print("-=:", x)
# updated value ==> 4

# *= operator
x *= 4
print("*=:", x)
# updated value ==> 16

# /= operator
x /= 2
print("/=:", x)
# updated value ==> 8

# %= operator
x %= 3
print("%=:", x)
# updated value ==> 2
