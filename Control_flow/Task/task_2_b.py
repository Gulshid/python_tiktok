#2. Find even & odd numbers using if condition inside loop
# Even Number 
print("Even Number ***")
for i in range(1, 21):
    if i % 2 == 0: # even logic
        print(i, end = " ")


print("\n")
# Odd Number
print("Odd Number ***")
for z in range(1, 21): # odd logic
    if z % 2 == 1:
        print(z, end = " ")